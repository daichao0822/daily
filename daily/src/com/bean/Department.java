package com.bean;

public class Department {
	private int g_intDepartmentId;
	private String g_strDepartmentName;
	private int g_intDepartmentHead;
	private String g_strDepartmentDescription;

	public int getG_intDepartmentId() {
		return g_intDepartmentId;
	}

	public void setG_intDepartmentId(int departmentId) {
		g_intDepartmentId = departmentId;
	}

	public String getG_strDepartmentName() {
		return g_strDepartmentName;
	}

	public void setG_strDepartmentName(String departmentName) {
		g_strDepartmentName = departmentName;
	}

	public int getG_intDepartmentHead() {
		return g_intDepartmentHead;
	}

	public void setG_intDepartmentHead(int departmentHead) {
		g_intDepartmentHead = departmentHead;
	}

	public String getG_strDepartmentDescription() {
		return g_strDepartmentDescription;
	}

	public void setG_strDepartmentDescription(String departmentDescription) {
		g_strDepartmentDescription = departmentDescription;
	}
}
